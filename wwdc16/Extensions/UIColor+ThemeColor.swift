import Foundation
import UIKit

extension UIColor {
    class func themeOrangeColor() -> UIColor {
        return UIColor(red: 223.0 / 255.0, green: 94.0 / 255.0, blue: 66.0 / 255.0, alpha: 1.0)
    }
    class func themePurpleColor() -> UIColor {
        return UIColor(red: 156.0 / 255.0, green: 108.0 / 255.0, blue: 158.0 / 255.0, alpha: 1.0)
    }
    class func themePlumColor() -> UIColor {
        return UIColor(red: 87.0 / 255.0, green: 87.0 / 255.0, blue: 147.0 / 255.0, alpha: 1.0)
    }
    class func themeBlueColor() -> UIColor {
        return UIColor(red: 84.0 / 255.0, green: 139.0 / 255.0, blue: 186.0 / 255.0, alpha: 1.0)
    }
    class func themeRedColor() -> UIColor {
        return UIColor(red: 212.0 / 255.0, green: 54.0 / 255.0, blue: 105.0 / 255.0, alpha: 1.0)
    }
}