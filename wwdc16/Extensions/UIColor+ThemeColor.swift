import Foundation
import UIKit

extension UIColor {
    class func themeBlueColor() -> UIColor {
        return UIColor(red: 40.0 / 255.0, green: 161.0 / 255.0, blue: 216.0 / 255.0, alpha: 1.0)
    }
    class func themeMarineColor() -> UIColor {
        return UIColor(red: 94.0 / 255.0, green: 202.0 / 255.0, blue: 230.0 / 255.0, alpha: 1.0)
    }
    class func themeAzureColor() -> UIColor {
        return UIColor(red: 56.0 / 255.0, green: 208.0 / 255.0, blue: 249.0 / 255.0, alpha: 1.0)
    }
    class func themeSkyBlueColor() -> UIColor {
        return UIColor(red: 135.0 / 255.0, green: 191.0 / 255.0, blue: 255.0 / 255.0, alpha: 1.0)
    }
    class func themePinkColor() -> UIColor {
        return UIColor(red: 232.0 / 255.0, green: 100.0 / 255.0, blue: 198.0 / 255.0, alpha: 1.0)
    }
}
